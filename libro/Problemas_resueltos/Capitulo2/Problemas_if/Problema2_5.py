print("Programa para saber si un numero es positivo, negativo o nulo")

NUM = int(input("Dame un número: "))

if  NUM > 0:
    print("Tu número es positivo")

elif NUM == 0:
    print("Tú número es nulo")

else:
    print("Tu número es negativo")

print("Fin del programa")


          
